CREATE TABLE IF NOT EXISTS sklep (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  lokalizacja VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS produkt (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  nazwa TEXT NOT NULL,
  typ TEXT NOT NULL,
  przeznaczenie TEXT NOT NULL,
  cena INTEGER NOT NULL
);

CREATE TABLE IF NOT EXISTS sprzedaz (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  kwota_sprzedazy DECIMAL(10,2) NOT NULL,
  data DATE,
  sklep_id INTEGER NOT NULL,
  FOREIGN KEY (sklep_id) REFERENCES sklep(id)
);

CREATE TABLE IF NOT EXISTS sprzedaz_produkt (
  sprzedaz_id INTEGER NOT NULL,
  produkt_id INTEGER NOT NULL,
  ilosc_sprzedana INTEGER NOT NULL,
  FOREIGN KEY (sprzedaz_id) REFERENCES sprzedaz(id),
  FOREIGN KEY (produkt_id) REFERENCES produkt(id),
  PRIMARY KEY (sprzedaz_id, produkt_id)
);

